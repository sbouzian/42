#include "libft.h"

/* English: Add the element new at the end of the list. */
/* Spanish: AÃ±ade el elemento new al final de la lista. */
void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*last;

	if (!lst || !new)
		return ;
	if (!*lst)
	{
		*lst = new;
		return ;
	}
	last = ft_lstlast(*lst);
	last->next = new;
}
