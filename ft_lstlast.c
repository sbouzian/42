#include "libft.h"

/* English: Return the last element of the list. */
/* Spanish: Devuelve el Ãºltimo elemento de la lista. */
t_list	*ft_lstlast(t_list *lst)
{
	if (!lst)
		return (NULL);
	while (lst->next)
		lst = lst->next;
	return (lst);
}
