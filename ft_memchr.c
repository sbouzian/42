#include "libft.h"

/* English: Search for the first occurrence of c in the initial n bytes of s. */
/* Spanish: Busca la primera ocurrencia de c en los primeros n bytes de s. */
void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t				i;
	const unsigned char	*ptr;

	ptr = (const unsigned char *)s;
	i = 0;
	while (i < n)
	{
		if (ptr[i] == (unsigned char)c)
			return ((void *)(ptr + i));
		i++;
	}
	return (NULL);
}
