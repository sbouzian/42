#include "libft.h"

/* English: Locate the first occurrence of character c in string s. */
/* Spanish: Localiza la primera ocurrencia del car√°cter c en la cadena s. */
char	*ft_strchr(const char *s, int c)
{
	while (*s)
	{
		if (*s == (char)c)
			return ((char *)s);
		s++;
	}
	if (*s == (char)c)
		return ((char *)s);
	return (NULL);
}
