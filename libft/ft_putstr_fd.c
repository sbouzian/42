#include "libft.h"

/* English: Write a string to the given file descriptor. */
/* Spanish: Escribe una cadena en el descriptor de archivo dado. */
void	ft_putstr_fd(char *s, int fd)
{
	if (!s)
		return ;
	write(fd, s, ft_strlen(s));
}
